            ontimeupdate={onTimeUpdate}
          ></audio>
        {/if}

        <!-- Transcript sync -->
        <div class="transcript-scroll">
          {#each report.transcript as seg, i}
            <div
              class="seg"
              class:active={activeSegIdx() === i}
              class:you={seg.speaker === 'You'}
              role="button"
              tabindex="0"
              onclick={() => seekTo(seg.start_ms)}
              onkeydown={(e) => { if (e.key === 'Enter') seekTo(seg.start_ms); }}
            >
              <span class="seg-speaker">{seg.speaker}</span>
              <span class="seg-time">{fmtDuration(seg.start_ms / 1000)}</span>
              <span class="seg-text">{seg.text}</span>
            </div>
          {/each}
        </div>
      </div>

      <!-- Q&A -->
      {#if report.qa_pairs.length > 0}
        <div class="section-label">Q&amp;A COACHING</div>
        {#if currentMs === 0 && report.qa_pairs.length > 0}
          <div class="qa-replay-hint">▶ Play recording to reveal coaching cards in real time</div>
        {/if}
        <div class="qa-list" bind:this={qaListEl}>
          {#each visibleQa as qa, i}
            {@const open = expandedQa === i}
            <div class="qa-item">
              <button class="qa-header" onclick={() => { expandedQa = open ? null : i; }}>
                <span class="qa-q">{qa.question}</span>
                <div class="qa-badges">
                  {#if qa.missed_followup}
                    <span class="badge miss">no follow-up</span>
                  {/if}
                  {#if qa.missed_metric}
                    <span class="badge miss">no metric</span>
                  {/if}
                  <span class="badge wpm" style="color:{wpmColor(qa.wpm)}">{qa.wpm} wpm · {fmtWpm(qa.wpm)}</span>
                  <span role="button" tabindex="0" class="seek-btn" title="Jump to question" onclick={(e) => { e.stopPropagation(); seekTo(qa.start_ms); }} onkeydown={(e) => { if (e.key === 'Enter' || e.key === ' ') { e.stopPropagation(); seekTo(qa.start_ms); } }}>⏩ {fmtDuration(qa.start_ms / 1000)}</span>
                  <span class="chevron">{open ? '▾' : '▸'}</span>
                </div>
              </button>
              {#if open}
                <div class="qa-body">
                  <div class="qa-answer-label">Your answer ({fmtDuration(qa.duration_secs)})</div>
                  <p class="qa-answer">{qa.answer_text}</p>
                  <div class="qa-coaching-label">Coaching</div>
                  <p class="qa-coaching">{qa.coaching}</p>
                </div>
              {/if}
            </div>
          {/each}
        </div>
      {/if}

    </div><!-- end modal-body -->

    <!-- Share footer -->
    <div class="share-footer">
      <a class="share-btn" href={downloadUrl} download>↓ Download .md</a>
      <button class="share-btn" class:copied={copyState === 'copied'} onclick={copyMarkdown}>
        {copyState === 'copied' ? '✓ Copied!' : 'Copy report'}
      </button>
      <div class="email-row">
        <input
          class="email-input"
          type="email"
          placeholder="Email report…"
          bind:value={emailTo}
          onkeydown={(e) => { if (e.key === 'Enter') sendEmail(); }}
        />
        <button class="send-btn" class:sent={emailSent} onclick={sendEmail} disabled={!emailTo.trim()}>
          {emailSent ? '✓' : '✉'}
        </button>
      </div>
      {#if onDelete}
        {#if confirmDelete}
          <button class="del-confirm-btn" onclick={doDelete}>Confirm delete</button>
          <button class="del-cancel-btn" onclick={() => { confirmDelete = false; }}>Cancel</button>
        {:else}
          <button class="del-btn" onclick={() => { confirmDelete = true; }}>Delete</button>
        {/if}
      {/if}
    </div>

  </div>
</div>

