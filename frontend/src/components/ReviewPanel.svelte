        <h2>{report.source_filename || 'Interview Review'}</h2>
        <span class="meta">
          {fmtDate(report.created_at)} · {fmtDuration(report.duration_secs)}
          {#if report.source_type === 'live'}
            <span class="badge live">Live</span>
          {:else}
            <span class="badge upload">Upload</span>
          {/if}
        </span>
